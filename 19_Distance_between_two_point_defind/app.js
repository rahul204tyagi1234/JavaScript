//.....Create a funcation to calculate the distance between two point 
//.....difined by their x,y cords.................
function numDistance (x1,y1,x2,y2){//1,2,9,8..............
    var a1 = x2-x1;//  9-1 =a1=8
    var a2 = y2-y1;// 8-2 = a2 = 6
    var num1 =a1*a1;//  8*8 = num1 = 64
    var num2 =a2*a2;// 6*6 =num2 =36

    return Math.sqrt(num1+num2);//(64+36)=100 of sqrt =10 
    // result == 10...........
}
